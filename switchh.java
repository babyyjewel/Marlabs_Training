class switchh{
  public static void main(String args[]){
           String choice = "hi";
          switch(choice)
	   {        
   		case "hi" :  System.out.println("Hi");
 				break;
		case "hello" :  System.out.println("Hello");
 				break; 
		case "bye" :  System.out.println("Bye");
 				break;
           }
}
}